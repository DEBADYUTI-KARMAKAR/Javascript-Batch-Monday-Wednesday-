let key = '/';
let number1 = 10;
let number2 = 20;
let res = 0;
switch (key) {
    case '+':
        res = number1+number2;
        console.log("Res=",res);
        break;
    case '-':
        res = number1-number2;
        console.log("Res=",res);
        break;
    default:
        console.log("Enter right choice");
}

